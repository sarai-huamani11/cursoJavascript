// CONCEPTO DE OPERADORES (SELEM)
// Los operadores permiten a los programas realizar cálculos complejos y tomar decisiones lógicas en función de comparaciones y otros tipos de condiciones.

// TIPOS DE OPERADORES (MAYKOL Y SELEM )

// 1.-Operadores aritméticos :
// (MAYKOL) Son los utilizados para la realización de operaciones matemáticas simples como la suma, resta o multiplicación. 
// (SELEM) se usan para calcular un valor de dos o más números, o cambiar el signo de un número de positivo a negativo o viceversa.
//+ Suma de dos valores
//- Resta de dos valores
//* Multiplicación de dos valores
//    / División de dos valores
//% El resto de la división de dos números (3%2 devolvería 1, el resto de dividir 3 entre 2)
//++ Incremento en una unidad, se utiliza con un solo operando
//-- Decremento en una unidad, utilizado con un solo operando

//2.- Operadores de asignación: 
// (MAYKOL) Sirven para asignar valores a las variables.
// (SELEM)Este operador se utiliza para guardar un valor específico en una variable.
//= Asignación. 
//+= Asignación con suma. 
//-= Asignación con resta
//*= Asignación de la multiplicación
// /= Asignación de la división
// %= Se obtiene el resto y se asigna

// 3.- Operadores de cadenas: 
//(MAYKOL) Las cadenas de caracteres, o variables de texto, también tienen sus propios operadores para realizar acciones típicas sobre cadenas.
// (SELEM) sirven para concatenar cadenas u objetos.
//+ Concatena dos cadenas, pega la segunda cadena a continuación de la primera.

// 4.- Operadores lógicos: 
// (MAYKOL) Estos operadores sirven para realizar operaciones lógicas, que son aquellas que dan como resultado un verdadero o un falso.Son el verdadero (true) y el falso (false). 
// (SELEM) son imprescindibles para realizar aplicaciones complejas, ya que se utilizan para tomar decisiones sobre las instrucciones que debería ejecutar el programa en función de ciertas condiciones.
//! Operador NO o negación. Si era true pasa a false y viceversa.
// && Operador Y, si son los dos verdaderos vale verdadero.
// || Operador O, vale verdadero si por lo menos uno de ellos es verdadero.

// 5.- Operadores condicionales: 
// (MAYKOL) Sirven para realizar expresiones condicionales todo lo complejas que deseemos.
// (SELEM) Es aquel que compara dos datos y nos indica la relación que hay entre ellos.
// == Comprueba si dos valores son iguales
// != Comprueba si dos valores son distintos
// > Mayor que, devuelve true si el primer operando es mayor que el segundo
// < Menor que, es true cuando el elemento de la izquierda es menor que el de la derecha
// >= Mayor igual
// <= Menor igual

// CONCLUSION  (SELEM)
// En conclusion los operadores junto con los operandos forman una expresión, que es una fórmula que define el cálculo de un valor. 
// CONCLUSION (MAYKOL)
// En conclusion los operadores pueden ser constantes, variables o llamadas a funciones, siempre que éstas devuelvan algún valor.

 